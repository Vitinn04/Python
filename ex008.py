n1 = float(input('Digite um valor: '))
n2 = float(input('Digite outro valor: '))
soma = n1 + n2
mult = n1 * n2
div = n1 / n2
divint = n1 // n2
poten = n1 ** n2
print(f'A soma é {soma}, o produto é {mult} e a divisão é {div:.3f}', end=' ')
print(f'A divisão inteira é {divint} e a potencia é {poten}')
